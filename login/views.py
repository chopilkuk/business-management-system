# Django 렌더링 함수 임포트
from django.shortcuts import render

def login_page(request):
    """
    로그인 페이지 뷰 함수
    
    사용자 로그인 페이지를 렌더링합니다.
    인증 폼과 로그인 처리 기능을 제공합니다.
    
    Args:
        request (HttpRequest): 클라이언트의 HTTP 요청 객체
        
    Returns:
        HttpResponse: 렌더링된 로그인 페이지 HTML 응답
        
    Description:
        - 사용자 로그인 페이지를 렌더링
        - 아이디/비밀번호 입력 폼 제공
        - 로그인 인증 처리
        - 세션 관리
        - 로그인 실패 시 에러 메시지 표시
        - 자동 로그인 옵션
        - 비밀번호 찾기 기능 연동
        
    Features:
        - 사용자 인증 폼
        - 세션 관리
        - 보안 처리
        - 에러 메시지 표시
        - 자동 로그인 기능
        - 비밀번호 재설정 링크
        
    Security Features:
        - CSRF 보호
        - 세션 보안
        - 비밀번호 암호화
        - 로그인 시도 제한
        
    Template:
        - login/login.html: 로그인 페이지 템플릿
    """
    # 로그인 페이지 템플릿을 렌더링하여 반환
    return render(request, 'login/login.html')