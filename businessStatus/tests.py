# Django 테스트 프레임워크 임포트
from django.test import TestCase

class BusinessStatusTests(TestCase):
    """
    업무 상태 앱 테스트 클래스
    
    업무 상태 관리 기능의 정상적인 동작을 검증하는 테스트 케이스를 포함합니다.
    페이지 접근, 뷰 함수 호출, 데이터베이스 작업 등을 테스트합니다.
    
    Test Methods:
        test_index_page: 업무 상태 메인 페이지 접근 테스트
        
    Description:
        - 업무 상태 페이지의 정상적인 렌더링 확인
        - HTTP 상태 코드 검증
        - 페이지 내용 검증
        - 뷰 함수 동작 확인
    """
    
    def test_index_page(self):
        """
        업무 상태 메인 페이지 테스트 메서드
        
        업무 상태 메인 페이지가 정상적으로 접근 가능한지 확인합니다.
        HTTP 상태 코드와 페이지 내용을 검증합니다.
        
        Returns:
            None: 테스트 결과는 자동으로 평가됩니다
            
        Raises:
            AssertionError: 테스트가 실패할 경우
            
        Test Steps:
            1. 업무 상태 페이지로 GET 요청 전송
            2. HTTP 상태 코드가 200인지 확인
            3. 페이지 내용에 '업무 상태 페이지'가 포함되는지 확인
        """
        # 업무 상태 페이지로 GET 요청 전송
        response = self.client.get('/businessStatus/')
        
        # HTTP 상태 코드가 200(정상)인지 확인
        self.assertEqual(response.status_code, 200)
        
        # 페이지 내용에 '업무 상태 페이지' 문자열이 포함되는지 확인
        self.assertContains(response, '업무 상태 페이지')
