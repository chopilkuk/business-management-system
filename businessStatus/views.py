# Django 렌더링 함수 임포트
from django.shortcuts import render

def index(request):
    """
    업무 상태 메인 페이지 뷰 함수
    
    Args:
        request (HttpRequest): 클라이언트의 HTTP 요청 객체
        
    Returns:
        HttpResponse: 렌더링된 업무 상태 페이지 HTML 응답
        
    Description:
        - 업무 상태 관리 메인 페이지를 렌더링
        - 프로젝트 및 업무의 현재 상태를 시각적으로 표시
        - 상태별 필터링 및 검색 기능 제공
        - 업무 상태 추가, 수정, 삭제 기능 지원
    """
    return render(request, 'businessStatus.html')
