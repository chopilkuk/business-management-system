# Django 관리자 모듈 임포트
from django.contrib import admin
# 현재 앱의 모델 임포트
from .models import BusinessStatus

# =============================================================================
# 관리자 클래스 정의
# =============================================================================
@admin.register(BusinessStatus)
class BusinessStatusAdmin(admin.ModelAdmin):
    """
    업무 상태 관리자 클래스
    
    Django 관리자 사이트에서 업무 상태 모델을 관리하는 설정을 정의합니다.
    목록 표시, 필터링, 검색 등의 기능을 설정합니다.
    
    Attributes:
        list_display (list): 관리자 목록 페이지에 표시할 필드
        list_filter (list): 필터링 옵션을 제공할 필드
        search_fields (list): 검색 기능을 제공할 필드
        
    Description:
        - 업무 상태 목록의 표시 형식 설정
        - 생성일 기준 필터링 기능 제공
        - 이름 기준 검색 기능 제공
        - 관리자 사이트의 사용자 경험 최적화
    """
    
    # 관리자 목록 페이지에 표시할 필드 목록
    # 이름, 설명, 생성일을 순서대로 표시하여 정보 파악 용이
    list_display = ['name', 'description', 'created_at']
    
    # 필터링 옵션을 제공할 필드 목록
    # 생성일을 기준으로 날짜별 필터링 기능 제공
    list_filter = ['created_at']
    
    # 검색 기능을 제공할 필드 목록
    # 이름 필드를 기준으로 검색 기능 제공
    search_fields = ['name']
